// Translation function
function translate(key, lang = 'ar') {
    return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
}

// Preserve original English text
function preserveOriginalText() {
    // For buttons, only preserve text if they have text content (not just icons)
    document.querySelectorAll('.btn').forEach(el => {
        // Skip buttons that only have icons (no text nodes or span elements with text)
        const hasTextContent = el.querySelector('span') || 
                              Array.from(el.childNodes).some(node => 
                                  node.nodeType === Node.TEXT_NODE && node.textContent.trim());
        if (hasTextContent && !el.dataset.originalText) {
            el.dataset.originalText = el.textContent.trim();
        }
    });
    
    // For accordion buttons, preserve the span text only
    document.querySelectorAll('.accordion-button span').forEach(el => {
        if (!el.dataset.originalText) {
            el.dataset.originalText = el.textContent.trim();
        }
    });
    
    // For other elements, preserve as before (but exclude accordion headers)
    document.querySelectorAll('.nav-link span, .card-title, .stat-label, .page-title, th, .dropdown-item, .badge, label, h1, h2, h3, h4, h5, h6, p, small, .small').forEach(el => {
        // Skip accordion headers - they have their own structure
        if (el.classList.contains('accordion-header')) {
            return;
        }
        // Skip if parent is accordion header
        if (el.closest('.accordion-header')) {
            return;
        }
        if (!el.dataset.originalText) {
            el.dataset.originalText = el.textContent.trim();
        }
    });
    
    document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
        if (!el.dataset.originalPlaceholder) {
            el.dataset.originalPlaceholder = el.placeholder;
        }
    });
}

// Apply translations to the page
function applyTranslations(lang = 'ar') {
    // First preserve original text if not already done
    preserveOriginalText();
    
    if (lang === 'en') {
        // Restore original English text - use a small delay for dynamically loaded elements
        const applyEnglish = () => {
            document.querySelectorAll('[data-original-text]').forEach(el => {
                // Skip accordion headers completely - they should never be translated directly
                if (el.classList.contains('accordion-header') || el.tagName === 'H2' && el.classList.contains('accordion-header')) {
                    return;
                }
                // Check if this is a span inside an accordion button
                if (el.tagName === 'SPAN' && el.parentElement && 
                    el.parentElement.classList.contains('accordion-button')) {
                    // Just update the text content of the span, preserving structure
                    el.textContent = el.dataset.originalText;
                }
                // For buttons with icons, only update text in span elements
                else if (el.tagName === 'BUTTON' && el.querySelector('i')) {
                    const textSpan = el.querySelector('span');
                    if (textSpan) {
                        textSpan.textContent = textSpan.dataset.originalText || el.dataset.originalText;
                    }
                } else {
                    el.textContent = el.dataset.originalText;
                }
            });
        };
        
        // Apply immediately
        applyEnglish();
        
        // And again after a short delay to catch any late-loading elements
        setTimeout(applyEnglish, 100);
        
        document.querySelectorAll('[data-original-placeholder]').forEach(el => {
            el.placeholder = el.dataset.originalPlaceholder;
        });
    } else {
        // Apply Arabic translations
        document.querySelectorAll('[data-original-text]').forEach(el => {
            const originalText = el.dataset.originalText;
            const translated = translate(originalText, lang);
            if (translated !== originalText) {
                // Skip accordion headers completely - they should never be translated directly
                if (el.classList.contains('accordion-header') || el.tagName === 'H2' && el.classList.contains('accordion-header')) {
                    return;
                }
                // Check if this is a span inside an accordion button
                if (el.tagName === 'SPAN' && el.parentElement && 
                    el.parentElement.classList.contains('accordion-button')) {
                    // Just update the text content of the span, preserving structure
                    el.textContent = translated;
                }
                // For buttons with icons, only update text in span elements
                else if (el.tagName === 'BUTTON' && el.querySelector('i')) {
                    const textSpan = el.querySelector('span');
                    if (textSpan) {
                        textSpan.textContent = translated;
                    }
                } else {
                    el.textContent = translated;
                }
            }
        });
        
        document.querySelectorAll('[data-original-placeholder]').forEach(el => {
            const originalText = el.dataset.originalPlaceholder;
            const translated = translate(originalText, lang);
            if (translated !== originalText) {
                el.placeholder = translated;
            }
        });
    }
}

// Export for use in other scripts
